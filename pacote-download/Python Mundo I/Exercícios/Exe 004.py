print('Tem somente espaços?', a.isspace())

print('É alfabético?', a.isalpha())

print('É um número?', a.isnumeric())

print('É alfanumérico?', a.isalnum())

print('Está em maiúsculas?', a.isupper())

print('Está em minúsculas?', a.islower())



